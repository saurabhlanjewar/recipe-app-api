"""
Calculation module for the application.
"""


def add(x, y):
    """
    Adds two numbers.

    :param x: First number
    :param y: Second number
    :return: Sum of x and y
    """
    return x + y


def subtract(x, y):
    """
    Subtracts the second number from the first.

    :param x: First number
    :param y: Second number
    :return: Result of x - y
    """
    return x - y
